#include <unistd.h>

void	ft_print_combn(int n)
{
	int	a [9];
	int	i;
	int	j;
	int	k;

	i = 0;
	j = n - 1;
	while (i < n)
	{
		a[i] = i + '0';
		i++;
	}
	write(1, &a, n);
}

int	main(void)
{
	ft_print_combn(5);
	return (0);
}
