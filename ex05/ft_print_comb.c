#include <unistd.h>

void	ft_print_comb(void)
{
	int	i;
	int	j;
	int	k;

	i = 0 + 48;
	while (i <= 7 + 48)
	{
		j = 1 + i;
		while (j <= 8 + 48)
		{
			k = 1 + j;
			while (k <= 9 + 48)
			{
				write(1, &i, 1);
				write(1, &j, 1);
				write(1, &k, 1);
				if (i != 55 || j != 56 || k != 57)
					write(1, ", ", 2);
				k++;
			}
			j++;
		}
		i++;
	}
}
